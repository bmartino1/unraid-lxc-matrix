#!/bin/bash
# =============================================================================
# BUILD PHASE - Stage 04: Matrix Synapse package installation only
# homeserver.yaml, signing key, DB config all happen in setup phase
# =============================================================================
set -euo pipefail
export DEBIAN_FRONTEND=noninteractive

echo "==> [04] Pre-seeding Synapse debconf with placeholder (overridden at setup)..."
echo "matrix-synapse-py3 matrix-synapse/server-name string localhost" \
  | debconf-set-selections
echo "matrix-synapse-py3 matrix-synapse/report-stats boolean false" \
  | debconf-set-selections

echo "==> [04] Installing Matrix Synapse..."
apt-get install -y matrix-synapse-py3

echo "==> [04] Stopping Synapse (will be configured and started at setup time)..."
systemctl stop matrix-synapse  2>/dev/null || true
systemctl disable matrix-synapse 2>/dev/null || true

echo "==> [04] Creating directory structure..."
mkdir -p /etc/matrix-synapse/conf.d
mkdir -p /var/lib/matrix-synapse/media_store
mkdir -p /var/log/matrix-synapse
chown -R matrix-synapse:matrix-synapse \
  /var/lib/matrix-synapse \
  /var/log/matrix-synapse \
  /etc/matrix-synapse

echo "==> [04] Removing placeholder config generated by package install..."
# We will write our own homeserver.yaml at setup time
rm -f /etc/matrix-synapse/homeserver.yaml
rm -f /etc/matrix-synapse/log.yaml

echo "==> Completed Stage 04 - Matrix Synapse installed (not yet configured)"
